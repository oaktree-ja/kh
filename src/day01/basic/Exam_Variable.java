package com.trump.day01.basic;

public class Exam_Variable {
	public static void main(String [] args) {
		/*
		 * 자료형 식별자;
		 * -> 변수의 선언
		 * 자료형 식별자 = 리터럴
		 * -> 변수의 선언 및 초기화
		 */
		int num = 3;
		char grade = 'B';
		String msg = "Hello Java";
		System.out.println(num);
		System.out.println(grade);
		System.out.println(msg);
	}
}
